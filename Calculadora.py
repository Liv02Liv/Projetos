num1 = int(input("Digite um Numero: "))
num2 = int(input("Digite outro Numero: "))

oper = input("Escolha um Operador; (+), (-), (*), (/): ")

if oper == "+":
    print(num1 + num2)

elif oper == "-":
    print(num1 - num2)

elif oper == "*":
    print(num1 * num2)

else:
    print(num1 / num2)

    
